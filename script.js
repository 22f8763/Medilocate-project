
  // Function to change text on hover
function changeText(element) {
    element.innerHTML = "Looking for Medicines?";
    element.style.color = "red"; // Change text color
    element.style.transition = "0.3s ease"; // Smooth effect
  }
  
  // Function to reset text when hover is removed
  function resetText(element) {
    element.innerHTML = "Find Your Medicines at the Nearest Pharmacies";
    element.style.color = "black"; // Reset text color
  }



  
  